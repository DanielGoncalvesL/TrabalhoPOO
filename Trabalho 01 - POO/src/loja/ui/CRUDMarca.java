    private void jEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jEditarMouseClicked
        // TODO add your handling code here:
        try {
            boolean ok = true;
            if (tfNome.getText().equals("")) {
                ok = false;
                tfNome.setBackground(Color.PINK);
            } else {
                tfNome.setBackground(Color.WHITE);
            }
            if (tfRepresentante.getText().equals("")) {
                ok = false;
                tfRepresentante.setBackground(Color.PINK);
            } else {
                tfRepresentante.setBackground(Color.WHITE);
            }
            if (ok) {
                String nome = tfNome.getText();
                String representante = tfRepresentante.getText();
                Marca marca = new Marca(nome, representante);
                int cod = (int) tbListarMarca.getValueAt(tbListarMarca.getSelectedRow(), 0);
                marca.setId(cod);
                if (sis.alterarMarca(marca, cod)) {
                    JOptionPane.showMessageDialog(this, "Produto Alterado com Sucesso!");
                    carregarMarcas();
                } else {
                    JOptionPane.showMessageDialog(this, "O Produto não foi Alterado!");
                    carregarMarcas();
                }
                reiniciarMenu();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Falha ao Alterar o Produto");
        }
    }//GEN-LAST:event_jEditarMouseClicked

