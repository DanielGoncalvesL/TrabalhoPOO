      private void carregarMarcas() {
        ArrayList<Marca> marcas = (ArrayList<Marca>) sis.listarMarca();
        DefaultTableModel modelo = (DefaultTableModel) tbListarMarca.getModel();
        modelo.setNumRows(0);
        marcas.forEach((_item) -> {
            modelo.addRow(new Object[]{
                _item.getId(),
                _item.getNome(),
                _item.getRepresentanteComercial(),
            });
        });
    }

       private void reiniciarMenu() {
        tfNome.setEnabled(false);
        tfNome.setText("");
        tfRepresentante.setEnabled(false);
        tfRepresentante.setText("");
        jNovo.setEnabled(true);
        jSalvar.setEnabled(false);
        jCancelar.setEnabled(false);
        tfNome.setBackground(Color.white);
        tfRepresentante.setBackground(Color.white);
        tfNome.setBackground(Color.white);
    }
      
