    private void CarregarProdutos(){
        produtos = (ArrayList<Produto>) sis.listar();
        DefaultTableModel modelo = (DefaultTableModel) tbListar.getModel();
        modelo.setNumRows(0);
        produtos.forEach((_item) -> {
            modelo.addRow(new Object[]{
                _item.getCodigo(),
                _item.getNome(),
                _item.getDescricao(),
               "R$ " + _item.getPreco(),
                _item.getQuantEstoque()
                
            });
        });
    }

